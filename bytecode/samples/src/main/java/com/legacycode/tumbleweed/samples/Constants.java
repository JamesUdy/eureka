package com.legacycode.tumbleweed.samples;

public class Constants {
  private static final String STRING = "Hello, World!";
  private static final int INT = 42;
  private static final long LONG = 42L;
  private static final double DOUBLE = 4.2;
  private static final float FLOAT = 4.2F;
  private static final int CHAR = 'A';
  private static final short SHORT = 8;
  private static final byte BYTE = 0x1;
  private static final boolean BOOLEAN = true;

  public static void main(String[] args) {
    System.out.println(STRING);
    System.out.println(INT);
    System.out.println(LONG);
    System.out.println(DOUBLE);
    System.out.println(FLOAT);
    System.out.println(CHAR);
    System.out.println(SHORT);
    System.out.println(BYTE);
    System.out.println(BOOLEAN);
  }
}
